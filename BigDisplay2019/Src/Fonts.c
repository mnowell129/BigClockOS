/**
 * @brief Draw one of the patterns above. For the big display.
 *
 * 
 * @author Charles "Mickey" Nowell (1/1/2020)
 * 
 * @param pattern 
 * @param row 
 * @param column 
 * @param color 
 */
void putPattern(const uint64_t *pattern, uint32_t row, uint32_t column,uint8_t color)
{
   int i, j;
   uint64_t  bit;
   uint64_t  shift;

   uint64_t mask;

   for(i = 0;i < 24;i++)
   {
      bit = 1;
      shift = 36;
      mask = bit << shift;
      for(j = 0;j < 24;j++)
      {
         if(pattern[i] & mask)
         {
            putPixel(drawFrame,row + i,column + j,color);
         }
         mask >>= 1;

      }
   }
}

