/**
 * @brief Flip from draw frame to show frame.
 *
 * 
 * @author Charles "Mickey" Nowell (1/1/2020)
 * 
 * @param void 
 */
void changeImagePlane(void)
{

   if(bufferIsPrimed)
   {
      showFrame ^= 1;
      drawFrame ^= 1;
      bufferIsPrimed = false;
   }

}


void primeTheBuffer(bool value)
{
   bufferIsPrimed = value;
}



void clearImage(uint32_t color)
{
   clearBuffer(drawFrame,color);
}



